function emitStoreByte87(code, loadBase, loadVal, args) {
  loadBase(args[0].v);
  loadVal(args[1].v);
  const off = args[2] ? args[2].v : 0;
  if (off === 0) {
    code.u8(0x88);
    code.u8(0x02);
  } else if (off >= -128 && off <= 127) {
    code.u8(0x88);
    code.u8(0x42);
    code.u8(off & 255);
  } else {
    code.u8(0x88);
    code.u8(0x82);
    code.u32(off);
  }
}

