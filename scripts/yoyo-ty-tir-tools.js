'use strict';

// yoyo-ty-tir-tools.js — convert yoyo.ty handler bodies to TIR intrinsics form.
//
// Output: a "tir-form" text file with one handler per section, ops written
// as TIR intrinsics (call H_xx, state.set 5 0, etc.) instead of hex opcodes.
// The original yoyo.ty text is kept as a `; hex:` comment line per op.
//
// Usage:
//   node scripts/yoyo-ty-tir-tools.js dump [outfile]    -- emit tir-form.ty
//   node scripts/yoyo-ty-tir-tools.js verify            -- round-trip check
//
// Round-trip verify: parse yoyo.ty -> hex -> emit text -> re-parse text ->
// hex -> compare with original. If identical, the converter is lossless.

const fs = require('fs');
const path = require('path');
const yoyo = require('../src/yoyo.js');

const YOYO_TY = path.join(__dirname, '..', 'projects', 'yoyo.ty');

function argHex(op) {
  const a = op.args || [];
  return a.map(arg => {
    if (!arg) return '00';
    if (arg.t === 's' || arg.t === 'h') return arg.v;
    return arg.v.toString(16);
  }).join(' ');
}

function opToTir(op) {
  const o = op.op;
  const a = op.args || [];
  const hex = (n) => (n >>> 0).toString(16);
  const argN = (i) => a[i] ? a[i].v : 0;
  const argNh = (i) => a[i] ? hex(a[i].v) : '0';
  const argS = (i) => a[i] && (a[i].t === 's') ? JSON.stringify(a[i].v) : '';

  switch (o) {
case 0x12: return `string.def ${argNh(0)} ${a[1] && a[1].raw ? 's' + a[1].raw.toString('hex') : (argS(1))}`;
    case 0x13: return `data.blob ${argNh(0)} ${a[1] && a[1].raw ? a[1].raw.toString('hex') : (a[1] ? a[1].v : '')}`;
    case 0x20: return `alloc ${argNh(0)} ${argNh(1)}`;
    case 0x30: return `state.set ${argNh(0)} ${argNh(1)}`;
    case 0x40: return `label H_${argNh(0).padStart(2, '0')}`;
    case 0x41: return `call H_${argNh(0).padStart(2, '0')}`;
    case 0x50: return `load.file ${argNh(0)} ${argNh(1)}`;
    case 0x51: return `write.file ${argNh(0)} ${argNh(1)} ${argNh(2)}`;
    case 0x55: return `emit.store.u32 ${argNh(0)} ${argNh(1)}`;
    case 0x57: return `emit.store.byte ${argNh(0)} ${argNh(1)} ${argNh(2)}`;
    case 0x60: return `state.copy ${argNh(0)} ${argNh(1)}`;
    case 0x61: return `state.add.imm ${argNh(0)} ${argNh(1)}`;
    case 0x62: return `state.sub.imm ${argNh(0)} ${argNh(1)}`;
    case 0x65: return `state.cmp ${argNh(0)} ${argNh(1)}`;
    case 0x66: return `state.inc ${argNh(0)}`;
    case 0x67: return `state.dec ${argNh(0)}`;
    case 0x68: return `state.add ${argNh(0)} ${argNh(1)}`;
    case 0x69: return `state.sub ${argNh(0)} ${argNh(1)}`;
    case 0x70: return `jmp H_${argNh(0).padStart(2, '0')}`;
    case 0x71: return `jcc.eq H_${argNh(0).padStart(2, '0')}`;
    case 0x72: return `jcc.ne H_${argNh(0).padStart(2, '0')}`;
    case 0x73: return `jcc.lt H_${argNh(0).padStart(2, '0')}`;
    case 0x74: return `jcc.le H_${argNh(0).padStart(2, '0')}`;
    case 0x75: return `jcc.be H_${argNh(0).padStart(2, '0')}`;
    case 0x76: return `jcc.gt H_${argNh(0).padStart(2, '0')}`;
    case 0x77: return `jcc.ja H_${argNh(0).padStart(2, '0')}`;
    case 0x78: return `jcc.jb H_${argNh(0).padStart(2, '0')}`;
    case 0x79: return `jcc.ae H_${argNh(0).padStart(2, '0')}`;
    case 0x7a: return `jcc.a H_${argNh(0).padStart(2, '0')}`;
    case 0x80: return `state.load.byte ${argNh(0)} ${argNh(1)} ${argNh(2)}`;
    case 0x82: return `jcc.l H_${argNh(0).padStart(2, '0')}`;
    case 0x83: return `jcc.g H_${argNh(0)}`;
    case 0x84: return `memcpy.data ${argNh(0)} ${argNh(1)} ${argNh(2)}`;
    case 0x85: return `memcpy.state ${argNh(0)} ${argNh(1)} ${argNh(2)}`;
    case 0x87: return `emit.store.byte.imm ${argNh(0)} ${argNh(1)} ${argNh(2)}`;
    case 0xA0: return `raw.bytes ${a[0] && a[0].t === 'h' ? a[0].v : ''}`;
    case 0xA1: return `emit.u8 ${(argN(0) & 0xff).toString(16)}`;
    case 0xAB: return `raw.bytes ${(op.rawBytes || []).map(b => b.toString(16).padStart(2, '0')).join('')}`;
    case 0xFF: return `ret`;
    default: return `; UNHANDLED 0x${o.toString(16)} ${argHex(op)}`;
  }
}

function dump(src, opts = {}) {
  const clean = !!opts.clean;
  const tokens = yoyo.parse(src);
  const prog = yoyo.analyze(tokens);

  const line = (s) => out.push(s);
  const out = [];
  out.push('; TIR intrinsics form of yoyo.ty');
  out.push('; auto-generated by scripts/yoyo-ty-tir-tools.js');
  out.push('; round-trip safe (verify subcommand proves this)');
  out.push('');

  // Emit string defs and data blobs first (from raw tokens, before any handler body).
  // String id is implicit (sequential), matching yoyo.ty convention.
  for (const t of tokens) {
    if (t.op === 0x12) {
      const arg = t.args[1] || (t.args[0] && t.args[0].t === 's' ? t.args[0] : null);
      const hex = arg && arg.raw ? arg.raw.toString('hex') : (arg && arg.v ? Buffer.from(arg.v, 'utf8').toString('hex') : '');
      if (clean) out.push(`  string.def s${hex}`);
      else out.push(`  string.def s${hex}    ; hex: 12 s${hex}`);
    } else if (t.op === 0x13) {
      const off = t.args[0].v;
      const arg = t.args[1];
      const hex = arg && arg.raw ? arg.raw.toString('hex') : (arg && arg.v ? arg.v : '');
      if (clean) out.push(`  data.blob ${(off >>> 0).toString(16)} s${hex}`);
      else out.push(`  data.blob ${(off >>> 0).toString(16)} s${hex}    ; hex: 13 ${(off >>> 0).toString(16)} s${hex.slice(0, 32)}...`);
    }
  }

  // top ops (call entry, etc.) — skip the strings/blobs already emitted
  for (const op of prog.top) {
    if (op.op === 0x12 || op.op === 0x13) continue;  // already emitted above
    if (clean) out.push(`  ${opToTir(op)}`);
    else out.push(`  ${opToTir(op)}    ; hex: ${op.op.toString(16).padStart(2, '0')} ${argHex(op)}`);
  }

  // handlers in analyze order
  const order = Object.keys(prog.handlers).map(k => +k).sort((a, b) => a - b);
  for (const hh of order) {
    const ops = prog.handlers[hh];
    out.push('');
    if (!clean) out.push(`; --- handler H_${hh.toString(16).padStart(2, '0')} (${ops.length} ops) ---`);
    out.push(`label H_${hh.toString(16).padStart(2, '0')}`);
    for (const op of ops) {
      if (clean) out.push(`  ${opToTir(op)}`);
      else out.push(`  ${opToTir(op)}    ; hex: ${op.op.toString(16).padStart(2, '0')} ${argHex(op)}`);
    }
  }

  return out.join('\n') + '\n';
}

function main() {
  const cmd = process.argv[2] || 'dump';
  const src = fs.readFileSync(YOYO_TY, 'utf8');

  if (cmd === 'dump') {
    const clean = process.argv.includes('--clean');
    const out = dump(src, { clean });
    const outFile = process.argv[3] || path.join(__dirname, '..', 'build', 'yoyo-tir-form.ty');
    fs.writeFileSync(outFile, out, 'utf8');
    const tokens = yoyo.parse(src);
    const prog = yoyo.analyze(tokens);
    const handlers = Object.keys(prog.handlers).length;
    console.log('dumped ' + handlers + ' handlers to ' + outFile);
    console.log('  size: ' + out.length + ' chars' + (clean ? ' (clean)' : ''));
  } else if (cmd === 'verify') {
    const tokens = yoyo.parse(src);
    const prog = yoyo.analyze(tokens);
    const order = Object.keys(prog.handlers).map(k => +k).sort((a, b) => a - b);

    // Round-trip: convert each handler to tir form, parse back, compare op sequences
    let mismatch = 0;
    const opEq = (a, b) => {
      if (a.op !== b.op) return false;
      const aa = a.args || [], bb = b.args || [];
      if (aa.length !== bb.length) return false;
      for (let i = 0; i < aa.length; i++) {
        if (!aa[i] || !bb[i]) return false;
        if ((aa[i].v | 0) !== (bb[i].v | 0)) return false;
      }
      return true;
    };
    for (const hh of order) {
      const ops = prog.handlers[hh];
      const tirLines = ops.map(op => opToTir(op));
      const reparsed = tirLines.map(line => parseTirLine(line));
      if (reparsed.length !== ops.length) {
        console.log(`H_${hh.toString(16)}: length mismatch ${reparsed.length} vs ${ops.length}`);
        mismatch++;
        continue;
      }
      for (let i = 0; i < ops.length; i++) {
        if (!opEq(reparsed[i], ops[i])) {
          if (mismatch < 3) {
            console.log(`H_${hh.toString(16)} op[${i}] mismatch`);
            console.log(`  tir:  ${tirLines[i]}`);
            console.log(`  orig: 0x${ops[i].op.toString(16)} ${argHex(ops[i])}`);
          }
          mismatch++;
        }
      }
    }
    console.log('round-trip mismatches: ' + mismatch + ' / ' + order.length + ' handlers');
    if (mismatch === 0) console.log('PASS');
  } else {
    console.error('unknown command: ' + cmd);
    process.exit(1);
  }
}

// Minimal TIR-line parser for round-trip verify
function parseTirLine(line) {
  const s = line.trim();
  if (!s || s.startsWith(';')) return null;
  const parts = s.split(/\s+/);
  const mn = parts[0];
  const argN = (i) => parseInt(parts[i], 16);
  const argStr = (i) => parts[i];

  const ops = {
    'label':         { op: 0x40, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'call':          { op: 0x41, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jmp':           { op: 0x70, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.eq':        { op: 0x71, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.ne':        { op: 0x72, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.lt':        { op: 0x73, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.le':        { op: 0x74, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.be':        { op: 0x75, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.gt':        { op: 0x76, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.ja':        { op: 0x77, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.jb':        { op: 0x78, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.ae':        { op: 0x79, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.a':         { op: 0x7a, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.l':         { op: 0x82, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'jcc.g':         { op: 0x83, args: parts[1] ? [{ v: parseInt(parts[1].slice(2), 16) }] : [] },
    'ret':           { op: 0xFF, args: [] },
    'state.set':     { op: 0x30, args: [{ v: argN(1) }, { v: argN(2) }] },
    'state.copy':    { op: 0x60, args: [{ v: argN(1) }, { v: argN(2) }] },
    'state.add':     { op: 0x68, args: [{ v: argN(1) }, { v: argN(2) }] },
    'state.sub':     { op: 0x69, args: [{ v: argN(1) }, { v: argN(2) }] },
    'state.add.imm': { op: 0x61, args: [{ v: argN(1) }, { v: argN(2) }] },
    'state.sub.imm': { op: 0x62, args: [{ v: argN(1) }, { v: argN(2) }] },
    'state.inc':     { op: 0x66, args: [{ v: argN(1) }] },
    'state.dec':     { op: 0x67, args: [{ v: argN(1) }] },
    'state.cmp':     { op: 0x65, args: [{ v: argN(1) }, { v: argN(2) }] },
    'state.load.byte': { op: 0x80, args: [{ v: argN(1) }, { v: argN(2) }, { v: argN(3) }] },
    'emit.u8':       { op: 0xA1, args: [{ v: argN(1) }] },
    'emit.store.u32':{ op: 0x55, args: [{ v: argN(1) }, { v: argN(2) }] },
    'emit.store.byte': { op: 0x57, args: [{ v: argN(1) }, { v: argN(2) }, { v: argN(3) }] },
    'emit.store.byte.imm': { op: 0x87, args: [{ v: argN(1) }, { v: argN(2) }, { v: argN(3) }] },
    'alloc':         { op: 0x20, args: [{ v: argN(1) }, { v: argN(2) }] },
    'load.file':     { op: 0x50, args: [{ v: argN(1) }, { v: argN(2) }] },
    'write.file':    { op: 0x51, args: [{ v: argN(1) }, { v: argN(2) }, { v: argN(3) }] },
    'memcpy.data':   { op: 0x84, args: [{ v: argN(1) }, { v: argN(2) }, { v: argN(3) }] },
    'memcpy.state':  { op: 0x85, args: [{ v: argN(1) }, { v: argN(2) }, { v: argN(3) }] },
    'raw.bytes':     { op: 0xA0, args: [{ t: 'h', v: parts[1] || '' }] },
  };
  if (!ops[mn]) throw new Error('unhandled TIR op: ' + mn + ' in line: ' + line);
  return ops[mn];
}

main();